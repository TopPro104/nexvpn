// Project lat/lng to x,y matching our Natural Earth SVG (800x400)
// Linear approximation of the projection used to generate country paths
export function projectLatLng(lat: number, lng: number): { x: number; y: number } {
  // X is linear: scale=2.17, center at x=400
  const x = 400 + lng * (390.6 / 180);
  // Y is nearly linear: scale=-2.49 per degree from center y=196.9
  const y = 196.9 - lat * (149.2 / 60);
  return { x, y };
}

